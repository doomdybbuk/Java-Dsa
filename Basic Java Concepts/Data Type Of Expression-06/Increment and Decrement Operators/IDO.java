

public class IDO {
    public static void main(String[] args) {
        int i = 56;
        System.out.println(i++);
        System.out.println(i);
        
        System.out.println("NEXT LINE");

        int b = 56;
        System.out.println(++b);
        System.out.println(b);

        System.out.println("NEXT LINE");

        int c = 56;
        System.out.println(c--);
        System.out.println(c);

        System.out.println("NEXT LINE");

        int d = 56;
        System.out.println(--d);
        System.out.println(d);

        int y = 7;
        int x = ++y + 8;
        System.out.println(x);
    }
}
